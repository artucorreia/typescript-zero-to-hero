// exemplo Null
let variavelNull: null = null;
console.log(variavelNull);
console.log(typeof variavelNull); // output: object

// exemplo Undefined
let variavelUndefined: undefined;
console.log(variavelUndefined);
console.log(typeof variavelUndefined); // output: undefined

// comparando
console.log(variavelUndefined == variavelNull); // outuput: true
console.log(variavelUndefined === variavelNull); // outuput: false
